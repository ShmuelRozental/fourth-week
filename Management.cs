using System;

namespace fourth_week
{

    public static class Management
    {
        public static GetDay(day)
        {
            switch (day)
            {
                case 1:
                    return day = "באחד בשבת"
                    break;
                case 2:
                    return day = "בשני בשבת"
                    break;
                case 3:
                    return day = "בשלישי בשבת"
                    break;
                case 4:
                    return day = "ברביעי בשבת"
                    break;
                case 5:
                    return day = "בחמישי בשבת"
                    break;
                case 6:
                    return day = "בששי בשבת"
                    break;
               
            }
        }
    }

}
